        {
            bool reval = false;
            if (thisValue != null && thisValue != DBNull.Value && bool.TryParse(thisValue.ToString(), out reval))
            {
                return reval;
            }
            return reval;
        }


        /// <summary>
        /// 
        /// </summary>
        /// <param name="thisValue"></param>
        /// <param name="fillDigits">填充位数</param>
        /// <returns></returns>
        public static string ObjToStringFill2(this int thisValue, int fillDigits = 2)
        {
            if (thisValue >= 0 && thisValue < 10)
            {
                return thisValue.ToString().PadLeft(fillDigits, '0');
            }
            return thisValue.ObjToString();
        }

        /// <summary>
        /// 时间比较
        /// true表示t1大
        /// </summary>
        /// <param name="t1"></param>
        /// <param name="t2"></param>
        /// <returns></returns>
        public static bool Time1SubTIme2(this DateTime t1, DateTime t2)
        {
            return DateTime.Compare(t1, t2) > 0;
        }

        /// <summary> 
        /// 根据时间戳获取时间 
        /// </summary>  
        public static DateTime TimeStampToDateTime(this long timeStamp)
        {
            if (timeStamp > 0)
            {
                return _dtStart.AddMilliseconds(timeStamp);
            }
            return DateTime.MinValue;
        }
